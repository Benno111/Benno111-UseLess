//! Minimal built-in monospaced bitmap font (5x7, padded to 8x8).
//! Only a subset of ASCII is explicitly drawn; other bytes fall back to a box.

pub const FONT_W: usize = 6; // 5px glyph + 1px spacer
pub const FONT_H: usize = 8; // 7px glyph + 1px spacer

/// Configurable font settings.
#[derive(Clone, Copy)]
pub struct FontConfig {
    /// Foreground color (RGB).
    pub fg: (u8, u8, u8),
    /// Optional background color (RGB). When `None`, background is left untouched.
    pub bg: Option<(u8, u8, u8)>,
    /// Integer scale factor (>=1) applied to both width and height.
    pub scale: u8,
    /// Extra pixels between glyphs horizontally.
    pub letter_spacing: u8,
}

impl Default for FontConfig {
    fn default() -> Self {
        Self {
            fg: (255, 255, 255),
            bg: None,
            scale: 1,
            letter_spacing: 1,
        }
    }
}

